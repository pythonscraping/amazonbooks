def strToFloat(strFloat):
        """
        get a float number from a string, such as Price:$5.99
        :param strFloat:
        :return: float number
        """
        import re
        f = re.compile(r'\d+\.?\d*')
        try:
            result = float(f.search(strFloat).group())
        except AttributeError:
            result = 0
        return result

